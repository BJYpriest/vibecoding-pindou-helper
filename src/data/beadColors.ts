export interface BeadColor {
  code: string;
  name: string;
  nameEn: string;
  hex: string;
  rgb: [number, number, number];
  category: 'standard' | 'pearl' | 'neon' | 'glow';
}

export interface BeadBrand {
  id: string;
  name: string;
  nameEn: string;
  description: string;
  colors: BeadColor[];
}

function hexToRgb(hex: string): [number, number, number] {
  const result = /^#?([a-f\d]{2})([a-f\d]{2})([a-f\d]{2})$/i.exec(hex);
  if (!result) return [0, 0, 0];
  return [
    parseInt(result[1], 16),
    parseInt(result[2], 16),
    parseInt(result[3], 16),
  ];
}

function c(
  code: string,
  name: string,
  nameEn: string,
  hex: string,
  category: BeadColor['category'] = 'standard'
): BeadColor {
  return { code, name, nameEn, hex, rgb: hexToRgb(hex), category };
}

